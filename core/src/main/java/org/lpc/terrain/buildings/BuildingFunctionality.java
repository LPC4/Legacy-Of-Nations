package org.lpc.terrain.buildings;

/**
 * Enum representing the different functionalities of buildings in the game.
 */
public enum BuildingFunctionality {
    /**
     * Defensive buildings are used for protection and defense.
     */
    DEFENSIVE,

    /**
     * Research buildings are used for scientific and technological advancements.
     */
    RESEARCH,

    /**
     * Resource gathering buildings are used to collect various resources.
     */
    RESOURCE_GATHERING,

    /**
     * Resource refining buildings are used to process and refine raw resources.
     */
    RESOURCE_REFINING
}
