package org.lpc.terrain.resources;

/**
 * Enum representing various types of resources available in the game.
 */
public enum ResourceType {
    /**
     * Wood resource type.
     */
    WOOD,

    /**
     * Stone resource type.
     */
    STONE,

    /**
     * Food resource type.
     */
    FOOD,

    /**
     * Gold resource type.
     */
    GOLD,

    /**
     * Iron resource type.
     */
    IRON
}
